#include <stdio.h>

/**
 * main - Entry point
 *
 * Return: Always 0 (Sucess)
 */
int main(void)
{
	char a;
	int b;
	long int c;
	long long int d;
	float e;

	printf("Size of a char: %lu byte(s)\n", (unsigned long)1(a));
	printf("Size of a int: %lu byte(s)\n", (unsigned long)4(b));
	printf("Size of a long int: %lu byte(s)\n", (unsigned long)4(c));
	printf("Size of a long long int: %lu byte(s)\n", (unsigned long)8(d));
	printf("Size of a float: %lu byte(s)\n", (unsigned long)4(e));
	return (0);
}
